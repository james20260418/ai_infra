#ifndef AI_INFRA_A_PLUS_B_H_
#define AI_INFRA_A_PLUS_B_H_

namespace demo {

// Computes the sum of two integers.
// @param a First integer.
// @param b Second integer.
// @return The sum of a and b.
int APlusB(int a, int b);

// Computes the sum of two floating-point numbers.
// @param a First floating-point number.
// @param b Second floating-point number.
// @return The sum of a and b.
float APlusB(float a, float b);

// Computes the sum of two double-precision floating-point numbers.
// @param a First double-precision number.
// @param b Second double-precision number.
// @return The sum of a and b.
double APlusB(double a, double b);

}  // namespace demo

#endif  // AI_INFRA_A_PLUS_B_H_
